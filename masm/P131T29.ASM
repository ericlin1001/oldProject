DSEG SEGMENT
VAR1 DB 10,?,10 DUP(?)
EQUMESS DB "VAR1=VAR2",0DH,0AH,'$'
NEQUMESS DB "VAR1!=VAR2",0DH,0AH,'$'
DSEG ENDS
EXTRA1 SEGMENT
VAR2 DB "ERICLIN"
LEN2 EQU $-VAR2
EXTRA1 ENDS
CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG,ES:EXTRA1
START:
MOV AX,DSEG
MOV DS,AX
MOV AX,EXTRA1
MOV ES,AX
LEA DX,VAR1
CALL INPUT
LEA SI,VAR1
LEA DI,VAR2
ADD SI,2
MOV AL,BYTE PTR [SI-1]
CMP AL,LEN2
JNE NEQU1
CLD
MOV CL,LEN2
REPE CMPSB
CMP CX,0
JE EQU1
JMP NEQU1

EQU1:
LEA DX,EQUMESS
MOV AH,09H
INT 21H
JMP EXIT
NEQU1:
LEA DX,NEQUMESS
MOV AH,09H
INT 21H

JMP EXIT

EXIT:
MOV AH,4CH
INT 21H
INPUT PROC
PUSH DX
PUSH AX
MOV AH,0AH
INT 21H
POP AX
POP DX
RET
INPUT ENDP
CSEG ENDS
END START
