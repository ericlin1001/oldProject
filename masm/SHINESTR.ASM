CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG
START:
MOV AX,DSEG
MOV DS,AX
MOV BX,10
NEXT:
CALL DELAY
MOV CX,25
DISP1:
MOV DX,OFFSET MESS1
MOV AH,09H
INT 21H
LOOP DISP1
MOV DX,OFFSET MESS2
MOV AH,09H
INT 21H
CALL DELAY
MOV CX,25
DISP2:
MOV DX,OFFSET MESS1
MOV AH,09H
INT 21H
LOOP DISP2
CALL DELAY
DEC BX
JNZ NEXT
MOV AH,4CH
INT 21H
;DELAY
DELAY PROC NEAR
 PUSH CX
 PUSH DX
 MOV CX,0FFFH
 LOOP1:
 MOV DX,0FFFFH
 LOOP2:
 NOP
 NOP
 NOP
 NOP
 NOP
 NOP
 NOP
 DEC DX
 JNZ LOOP2
 LOOP LOOP1
 POP DX
 POP DX
 RET
 DELAY ENDP

EXIT:
MOV AH,4CH
INT 21H
CSEG ENDS
END START
