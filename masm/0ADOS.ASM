DSEG SEGMENT
BUFFER DB 6,?,6 DUP(?),'$'
MESS1 DB "WHAT YOU HAS INPUT IS:$"
MESS2 DB "THE LENGTH IS:$"
CR DB 0DH,0AH,'$'
DSEG ENDS

CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG
START:
MOV AX,DSEG
MOV DS,AX
MOV SI,OFFSET BUFFER
MOV DX,SI
MOV AH,0AH
INT 21H
MOV DX,OFFSET MESS1
CALL DISP
MOV DX,[SI+2]
CALL DISP
MOV DX,OFFSET CR
CALL DISP
LEA DX,MESS2
CALL DISP
MOV DX,0
MOV DL,[SI+1]
MOV AH,2
INT 21H
LEA DX,CR
CALL DISP

EXIT:
MOV AH,4CH
INT 21H

DISP PROC NEAR
MOV AH,09
INT 21H
RET
DISP ENDP
CSEG ENDS
END START
