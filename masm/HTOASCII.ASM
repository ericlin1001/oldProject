DSEG SEGMENT
VAR1 DT 0A0B0C0D0E0F01020304H
NUM DB 10D
DSEG ENDS

CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG
START:
MOV AX,DSEG
MOV DS,AX
MOV CX,0
MOV CL,NUM
LEA SI,VAR1
AGAIN:
MOV DL,BYTE PTR [SI]
INC SI
CALL HTOASCII
MOV AH,2
INT 21H
LOOP AGAIN
EXIT:
MOV AH,4CH
INT 21H

;
HTOASCII PROC NEAR
AND DL,0FH
CMP DL,10D
JB NUMBER
ADD DL,7H
NUMBER:
ADD DL,30H
RET
HTOASCII ENDP
;

CSEG ENDS
END START
