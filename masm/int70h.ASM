CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG,ES:EXTRA1
START:
MOV AX,DSEG
MOV DS,AX
MOV AX,EXTRA1
MOV ES,AX
MOV AX,70H*4
MOV BX,AX
MOV AX,0
MOV ES,AX
MOV AX,OFFSET INT70H
MOV ES:[BX],AX
MOV AX,CS
MOV ES:[BX+2],AX
