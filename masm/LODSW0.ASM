DSEG SEGMENT
VAR1 DB "ABCXYZ"
LEN EQU $-VAR1
DSEG ENDS
EXTRA1 SEGMENT

EXTRA1 ENDS
CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG,ES:EXTRA1
START:
MOV AX,DSEG
MOV DS,AX
MOV AX,EXTRA1
MOV ES,AX
LEA SI,VAR1
MOV CX,LEN
LOOP1:
LODSB
MOV DL,AL
CMP DL,'Z'
JE EQUZ
NQUZ:
ADD DL,1
JMP NEXT
EQUZ:
SUB DL,25D
JMP NEXT
NEXT:
CALL DISPB
LOOP LOOP1

EXIT:
MOV AH,4CH
INT 21H
DISPB PROC
PUSH AX
MOV AH,2
INT 21H
POP AX
RET
DISPB ENDP

CSEG ENDS
END START
