DSEG SEGMENT
MESS1 DB "ERROR!$"
DSEG ENDS

CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG
START:
MOV AX,DSEG
MOV DS,AX
CALL INPUT
CALL COMPARE
ADD BL,0
JZ ERR
SUB BL,1
JZ UPPER
SUB BL,1
JZ LOWER

UPPER:
ADD AL,20H
MOV DL,AL
CALL DISP
JMP EXIT
LOWER:
SUB AL,20H
MOV DL,AL
CALL DISP
JMP EXIT
ERR:
MOV DX,OFFSET MESS1
MOV AH,09H
INT 21H
JMP EXIT


EXIT:
MOV AH,4CH
INT 21H

;INPUT -AL
INPUT PROC NEAR
MOV AH,1
INT 21H
RET
INPUT ENDP
;COMPARE AL-BL(0:ERROR,1:UPPERCASE,2:LOWERCASE)
COMPARE PROC
PUSH AX
CMP AL,'A'
JB CASE1
CMP AL,'Z'
JA CASE2
MOV BL,1
JMP COMPAREEXIT
CASE2:
CMP AL,'a'
JB CASE1
CMP AL,'z'
JA CASE1
MOV BL,2
JMP COMPAREEXIT
CASE1:
MOV BL,0
JMP COMPAREEXIT
COMPAREEXIT:
POP AX
RET
COMPARE ENDP
;DISP DL-
DISP PROC
MOV AH,2
INT 21H
RET
DISP ENDP
CSEG ENDS
END START
